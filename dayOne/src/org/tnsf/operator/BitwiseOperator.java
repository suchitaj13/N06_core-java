//program to demonstrate on bitwise and shifting operator

package org.tnsf.operator;

public class BitwiseOperator {

	public static void main(String[] args) {
    int x=56,y=5;
    System.out.println(x&y);//and
    System.out.println(x|y);//or
    System.out.println(x^y);
    System.out.println(8>>1);//left
    System.out.println(8<<1);//right
	}

}
